function mapLower(map, val) {
  if (!val) return null;
  return map[val.toLowerCase().trim()] || null;
}

function translateComposition(text) {
  let result = text.toLowerCase();
  for (const [ru, ua] of Object.entries(COMP_RU_UA)) {
    result = result.replace(new RegExp('\\b' + ru + '\\b', 'g'), ua);
  }
  return result;
}

// Конвертує HTML-таблицю замірів у читабельний текст з <br>
// Кожен рядок таблиці → "Назва: val1 | val2 | ..." (окремий рядок на KASTA)
function htmlTableToText(html) {
  let text = html.replace(/<style[\s\S]*?<\/style>/gi, '');

  const rows = [];
  text = text.replace(/<tr[^>]*>([\s\S]*?)<\/tr>/gi, (_, rowHtml) => {
    const cells = [];
    rowHtml.replace(/<t[dh][^>]*>([\s\S]*?)<\/t[dh]>/gi, (__, cellHtml) => {
      const ct = cellHtml.replace(/<[^>]+>/g, '').replace(/\s+/g, ' ').trim();
      if (ct) cells.push(ct);
    });
    if (cells.length === 1)      rows.push(cells[0]);
    else if (cells.length > 1)   rows.push(cells[0] + ': ' + cells.slice(1).join(' | '));
    return '';
  });

  // Прибираємо залишкові структурні теги
  text = text.replace(/<\/?(table|thead|tbody|tfoot)[^>]*>/gi, ' ');
  text = text.replace(/<\/?(p|div|h[1-6]|li|ul|ol|br)[^>]*>/gi, ' ');
  text = text.replace(/<[^>]+>/g, '');
  text = text.replace(/\s+/g, ' ').trim();

  const parts = [];
  if (text) parts.push(text);
  if (rows.length) parts.push(rows.join('<br>'));
  return parts.join('<br>');
}

function cleanDescTag(offer, tag) {
  const re = new RegExp(`<${tag}><\\!\\[CDATA\\[([\\s\\S]*?)\\]\\]><\\/${tag}>`, 'gi');
  return offer.replace(re, (_, cdata) => {
    const text = htmlTableToText(cdata).slice(0, 4900);
    return text ? `<${tag}><![CDATA[${text}]]></${tag}>` : `<${tag}></${tag}>`;
  });
}

function buildHeader(raw) {
  return raw
    .replace(/<!DOCTYPE[^>]*>/gi, '')
    .replace(/<phone>[^<]*<\/phone>/gi, '')
    .replace(/<currency[^>]*id=['"]?USD['"]?[^>]*\/>/gi, '')
    .replace(/=\s*'([^']*)'/g, '="$1"')
    .trim() + '\n';
}

function transformOffer(raw) {
  let offer = raw;

  // Одинарні лапки → подвійні (лише атрибути)
  offer = offer.replace(/=\s*'([^']*)'/g, '="$1"');

  // Витягуємо назви ДО перейменування (для derivePattern)
  const nameRuM  = offer.match(/<name>([\s\S]*?)<\/name>/);
  const nameUaM  = offer.match(/<name_ua>([\s\S]*?)<\/name_ua>/);
  const nameText = ((nameRuM && nameRuM[1]) || (nameUaM && nameUaM[1]) || '').toLowerCase();

  // Чистимо описи (рос + укр)
  offer = cleanDescTag(offer, 'description');
  offer = cleanDescTag(offer, 'description_ua');

  // <name>Рус</name> → <name_ru>Рус</name_ru> (тільки точний тег, не name_ua)
  offer = offer.replace(/<name>([\s\S]*?)<\/name>/g, '<name_ru>$1</name_ru>');

  // <sku> → <article>
  offer = offer.replace(/<sku>([\s\S]*?)<\/sku>/gi, '<article>$1</article>');

  // Прибираємо теги, яких нема в KASTA
  offer = offer.replace(/<pickup>[^<]*<\/pickup>/gi, '');
  offer = offer.replace(/<delivery>[^<]*<\/delivery>/gi, '');
  offer = offer.replace(/<url>[^<]*<\/url>/gi, '');

  // Категорія
  const catM = offer.match(/<categoryId>(\d+)<\/categoryId>/);
  const catId = catM ? catM[1] : '';

  // ── Ціна ─────────────────────────────────────────────────
  const mPrice = offer.match(/<price>([\d.,]+)<\/price>/);
  if (!mPrice) return null;
  const srcPrice = parseFloat(String(mPrice[1]).replace(',', '.'));
  if (isNaN(srcPrice)) return null;

  if (srcPrice < MIN_PRICE) {
    return offer.replace(/\bavailable="true"/, 'available="false"');
  }

  const newPrice   = Math.round(srcPrice * MULT + ADD);
  const promoPrice = Math.round(newPrice * PROMO_K);

  offer = offer.replace(
    /<price>[\d.,]+<\/price>/,
    `<price>${newPrice}</price>\n\t\t\t<price_promo>${promoPrice}</price_promo>`
  );

  offer = offer.replace(/<oldprice>([\d.,]+)<\/oldprice>/gi, (_, val) => {
    const srcOld = parseFloat(String(val).replace(',', '.'));
    if (isNaN(srcOld)) return '';
    const computed = Math.round(srcOld * MULT + ADD);
    const finalOld = computed > newPrice ? computed : Math.round(newPrice * 1.20);
    return `<old_price>${finalOld}</old_price>`;
  });

  // ── Витягуємо всі існуючі <param> ───────────────────────
  const params = {};
  offer = offer.replace(/\s*<param name=['"]([^'"]+)['"]>([^<]*)<\/param>/g, (_, name, value) => {
    params[name.trim()] = value.trim();
    return '';
  });

  // ── Будуємо KASTA-param блок ─────────────────────────────
  const kp = [];

  // Колір
  const colorRaw = params['Цвет'] || params['Колір'] || '';
  const color = mapLower(COLOR_MAP, colorRaw) || colorRaw;
  if (color) kp.push(`<param name="Колір">${color}</param>`);

  // Розмір (міжнародний)
  const sizeIntl = params['Размер'] || params['Международный размер'] || params['Розмір'] || '';
  if (sizeIntl) kp.push(`<param name="Розмір">${sizeIntl}</param>`);

  // Розмір KASTA (числовий жіночий)
  const sizeNum = params['Размер женской одежды'] || '';
  if (sizeNum && sizeNum !== sizeIntl) kp.push(`<param name="Розмір Kasta">${sizeNum}</param>`);

  // Матеріал
  const fabricRaw = params['Тип ткани'] || params['Матеріал'] || '';
  const fabric = mapLower(MATERIAL_MAP, fabricRaw) || fabricRaw;
  if (fabric) kp.push(`<param name="Матеріал">${fabric}</param>`);

  // Сезонність
  const seasonRaw = params['Сезон'] || params['Сезонність'] || '';
  const season = mapLower(SEASON_MAP, seasonRaw) || 'Демісезон';
  kp.push(`<param name="Сезонність">${season}</param>`);

  // Стиль (може бути множинний через "; ")
  const styleRaw = params['Стиль'] || '';
  if (styleRaw) {
    const styles = styleRaw.split(/[;,]/).map(s => s.trim()).filter(Boolean);
    const added = new Set();
    for (const s of styles) {
      const mapped = mapLower(STYLE_MAP, s);
      if (mapped && !added.has(mapped)) {
        kp.push(`<param name="Стиль">${mapped}</param>`);
        added.add(mapped);
      }
    }
  }

  // Застібка
  const fasteningRaw = params['Застежка'] || params['Застібка'] || '';
  const fastening = mapLower(FASTENING_MAP, fasteningRaw);
  if (fastening) kp.push(`<param name="Застібка">${fastening}</param>`);

  // Країна виробника
  const countryRaw = params['Страна-производитель товара'] || params['Страна производитель'] || params['Країна виробництва'] || '';
  const country = mapLower(COUNTRY_MAP, countryRaw) || countryRaw;
  if (country) kp.push(`<param name="Країна виробництва">${country}</param>`);

  // Склад (переклад рос → укр)
  const comp = params['Состав'] || params['Склад'] || '';
  if (comp) kp.push(`<param name="Склад">${translateComposition(comp)}</param>`);

  // Декор
  const decorRaw = params['Декорирование'] || params['Декор'] || '';
  const decor = mapLower(DECOR_MAP, decorRaw);
  if (decor) kp.push(`<param name="Декор">${decor}</param>`);

  // ── Додаткові params AGER (Довжина виробу, Комір, Крій тощо) ─
  // Якщо AGER передає ці поля — перекладаємо і зберігаємо
  for (const [rawName, rawValue] of Object.entries(params)) {
    if (KNOWN_PARAMS.has(rawName)) continue; // вже оброблено вище
    if (SKIP_PARAMS.has(rawName))  continue; // не потрібен KASTA
    if (!rawValue) continue;
    const uaName  = PARAM_NAME_MAP[rawName] || rawName;
    const vMap    = PARAM_VALUE_MAPS[uaName];
    const uaValue = vMap ? (vMap[rawValue.toLowerCase().trim()] || rawValue) : rawValue;
    kp.push(`<param name="${uaName}">${uaValue}</param>`);
  }

  // ── Візерунок* (обов'язковий для сукень у KASTA) ────────
  if (DRESS_CATS.has(catId)) {
    const pattern = derivePattern(params['Декорирование'] || '', nameText);
    kp.push(`<param name="Візерунок">${pattern}</param>`);
  }

  // Вставляємо params перед </offer>
  if (kp.length > 0) {
    const block = '\n' + kp.map(p => '\t\t\t' + p).join('\n') + '\n\t\t';
    offer = offer.replace('</offer>', block + '</offer>');
  }

  return offer;
}

// Визначаємо візерунок з типу декору та назви товару
function derivePattern(decorRaw, nameText) {
  const d = decorRaw.toLowerCase();
  const n = nameText;

  // Перевіряємо назву (більш конкретна інформація)
  if (n.includes('цветочн') || n.includes('квіткови'))   return 'Квітковий';
  if (n.includes('полоск')  || n.includes('смужк'))      return 'Смужка';
  if (n.includes('горошек') || n.includes('горошок'))    return 'Горошок';
  if (n.includes('клетк')   || n.includes('клітинк'))    return 'Клітинка';
  if (n.includes('леопард'))                              return 'Леопардовий';
  if (n.includes('зебр'))                                 return 'Зебра';
  if (n.includes('тай-дай') || n.includes('тай дай'))    return 'Тай-дай';
  if (n.includes('камуфляж'))                             return 'Камуфляжний';
  if (n.includes('геометр'))                              return 'Геометричний';
  if (n.includes('абстракт'))                             return 'Абстрактний';
  if (n.includes('смарагд') || n.includes('орнамент'))   return 'Орнамент';
  if (n.includes('градієнт') || n.includes('градиент'))  return 'Градієнт';

  // Перевіряємо декорування
  if (d.includes('цветочн'))   return 'Квітковий';
  if (d.includes('принт'))     return 'Малюнок';
  if (d.includes('вышивк'))    return 'Малюнок';
  if (d.includes('однотон'))   return 'Однотонний';

  // Якщо принт не знайдено — однотонна (найпоширеніше)
  return 'Однотонний';
}

// ── Завантаження та потокова обробка ─────────────────────────
function downloadAndProcess() {
  return new Promise((resolve, reject) => {
    console.log('Завантажуємо:', SOURCE_URL);

    https.get(SOURCE_URL, { headers: { 'User-Agent': 'KastaFeedBuilder/2.0' } }, (res) => {
      if (res.statusCode !== 200) {
        reject(new Error('HTTP ' + res.statusCode));
        return;
      }

      let buf     = '';
      let hdrDone = false;
      const parts = [];

      res.setEncoding('utf8');

      res.on('data', (chunk) => {
        buf += chunk;

        // Фаза 1: заголовок (до <offers>)
        if (!hdrDone) {
          const idx = buf.indexOf('<offers>');
          if (idx === -1) {
            if (buf.length > 262144) buf = buf.slice(-2048);
            return;
          }
          parts.push(buildHeader(buf.slice(0, idx)) + '<offers>\n');
          buf     = buf.slice(idx + 8);
          hdrDone = true;
        }

        // Фаза 2: оффери — обробляємо кожен одразу при надходженні
        for (;;) {
          const s = buf.indexOf('<offer ');
          if (s === -1) { if (buf.length > 16384) buf = buf.slice(-1024); break; }

          const e = buf.indexOf('</offer>', s);
          if (e === -1) { buf = buf.slice(s); break; }

          const raw = buf.slice(s, e + 8);
          buf       = buf.slice(e + 8);

          // Беремо тільки available=true
          if (!/available=['"]true['"]/.test(raw)) continue;

          const out = transformOffer(raw);
          if (out) parts.push(out + '\n');
        }
      });

      res.on('end', () => {
        parts.push('</offers>\n</shop>\n</yml_catalog>\n');
        resolve(parts.join(''));
      });

      res.on('error', reject);
    }).on('error', reject);
  });
}

async function main() {
  const t0 = Date.now();
  const xml = await downloadAndProcess();

  fs.writeFileSync(OUT_FILE, xml, 'utf8');

  const sizeMB = (Buffer.byteLength(xml, 'utf8') / 1024 / 1024).toFixed(2);
  const sec    = ((Date.now() - t0) / 1000).toFixed(1);
  console.log(`Готово! ${OUT_FILE} → ${sizeMB} MB за ${sec}с`);
}

main().catch(err => { console.error(err); process.exit(1); });
